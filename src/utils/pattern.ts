/**
 * http url pattern
 */
export const HttpPattern = /^(http|https):\/\/.+/;
/**
 * assets pattern
 */
export const AssetsPattern = /^assets\/./;

/**
 * id number pattern
 */
export const IdNumPattern = /^[0-9]{17}[0-9Xx]$/;

/**
 * phone number pattern
 */
export const PhonePattern = /^1\d{10}$/;

/**
 * email pattern
 */
export const EmailPattern = /^.+@.+\..+$/i;
